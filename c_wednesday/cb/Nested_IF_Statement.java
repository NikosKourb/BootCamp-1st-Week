package cb;

//Program that returns a String when the statement that we have set is true 
//and also another one is also true
public class Nested_IF_Statement {

	public static void main(String[] args) {
		
		int x=45;
		int y=67;
		
		if(x==45) {
			if(y==67) {
				System.out.println("The value of x is 45 and the value of y is 67 !!!");
			}
		}

	}

}